export const decodedValue = (colors) => {
  const COLORS = {
    black: 0,
    brown: 1,
    red: 2,
    orange: 3,
    yellow: 4,
    green: 5,
    blue: 6,
    violet: 7,
    grey: 8,
    white: 9,
  };
  let answer = "";
  for (let x = 0; x < 2; x++) answer += COLORS[colors[x]];

  return parseInt(answer);
};
